#include<stdio.h>

int main() {

    printf("relational operator(true):%d \n", 4 ==3); // returns true
    

    printf("relational operator(false):%d\n",4==4);  // returns false

    printf("relational operator:%d\n",4>3);
    printf("relational operator:%d\n",3>4);
    printf("relational operator:%d\n",3>=3);
    
    return 0;
}
